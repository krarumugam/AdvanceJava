DROP TABLE IF EXISTS BILL_TO_ADDR;
DROP TABLE IF EXISTS CUSTOMER;
DROP SEQUENCE IF EXISTS CUSTOMER_SEQ;
DROP SEQUENCE IF EXISTS BILL_TO_ADDR_SEQ;

CREATE SEQUENCE CUSTOMER_SEQ start with 1 increment by 1;
CREATE TABLE IF NOT EXISTS CUSTOMER (
  cust_id bigint DEFAULT CUSTOMER_SEQ.nextval primary key,
  name VARCHAR(250) NOT NULL,
  email VARCHAR(250) DEFAULT NULL
);

CREATE SEQUENCE BILL_TO_ADDR_SEQ start with 1 increment by 1;
CREATE TABLE IF NOT EXISTS BILL_TO_ADDR (
  bill_to_addr_id bigint DEFAULT BILL_TO_ADDR_SEQ.nextval primary key,
  cust_id INT NOT NULL,
  name VARCHAR(250) NOT NULL
);